

object DataTypesExample {
  def main(args: Array[String]) {
    val intVal: Int = 15
    val doubleVal: Double = 3.14
    val charVal: Char = 'A'
    val stringVal: String = "Kumar"
    val boolVal: Boolean = true
    
    println(s"Int: $intVal, Double: $doubleVal, Char: $charVal, String: $stringVal, Boolean: $boolVal")
  }
}